let currentLang = 'en';
let currentPage = document.documentElement.dataset.currentPage || 'home';

function applyTranslations(lang) {
  currentLang = lang;
  const t = translations[lang];

  // Text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // HTML content (elements with nested tags like <strong>)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // HTML lang attribute and page title
  document.documentElement.lang = lang === 'en' ? 'en' : 'pt-BR';
  const titleKey = 'title-' + currentPage;
  if (translations[lang][titleKey]) document.title = translations[lang][titleKey];

  // Toggle button label (shows the language you can switch TO)
  document.getElementById('langToggle').textContent = lang === 'en' ? 'PT' : 'EN';

  // Keep experience expand button label in sync
  const expBtn = document.getElementById('expToggle');
  if (expBtn) expBtn.textContent = translations[lang][expExpanded ? 'exp-show-less' : 'exp-show-more'];
}

// ===== LANGUAGE TOGGLE =====
document.getElementById('langToggle').addEventListener('click', () => {
  applyTranslations(currentLang === 'en' ? 'pt' : 'en');
});

// ===== EXPERIENCE EXPAND =====
let expExpanded = false;
const _expToggleBtn = document.getElementById('expToggle');
if (_expToggleBtn) {
  _expToggleBtn.addEventListener('click', () => {
    expExpanded = !expExpanded;
    const extra = document.getElementById('timelineExtra');
    const btn   = document.getElementById('expToggle');
    extra.classList.toggle('open', expExpanded);
    btn.textContent = translations[currentLang][expExpanded ? 'exp-show-less' : 'exp-show-more'];
  });
}

// ===== NAVIGATION =====
function navigate(page) {
  currentPage = page;
  document.title = translations[currentLang]['title-' + page];

  document.querySelectorAll('.page').forEach(p => p.classList.remove('active'));
  const target = document.getElementById('page-' + page);
  if (target) target.classList.add('active');

  document.querySelectorAll('.nav-link, .mobile-link').forEach(link => {
    link.classList.toggle('active', link.dataset.page === page);
  });

  document.getElementById('mobileMenu').classList.remove('open');
  window.scrollTo({ top: 0, behavior: 'smooth' });
}

// Nav links click — only intercept SPA links (those with data-page); real href links navigate naturally
document.querySelectorAll('.nav-link, .mobile-link').forEach(link => {
  link.addEventListener('click', (e) => {
    if (link.dataset.page) {
      e.preventDefault();
      navigate(link.dataset.page);
    }
  });
});

// Hero buttons
document.querySelectorAll('[data-goto]').forEach(btn => {
  btn.addEventListener('click', () => navigate(btn.dataset.goto));
});

// Mobile menu toggle
document.querySelector('.menu-toggle').addEventListener('click', () => {
  document.getElementById('mobileMenu').classList.toggle('open');
});

// ===== CONTACT FORM =====
const _contactForm = document.getElementById('contactForm');
if (_contactForm) _contactForm.addEventListener('submit', async function (e) {
  e.preventDefault();

  const name    = document.getElementById('name').value.trim();
  const email   = document.getElementById('email').value.trim();
  const message = document.getElementById('message').value.trim();

  if (!name || !email || !message) return;

  const btn = this.querySelector('button[type="submit"]');
  btn.textContent = translations[currentLang]['form-sending'];
  btn.disabled = true;

  try {
    const res = await fetch('https://formspree.io/f/mkoqazql', {
      method: 'POST',
      headers: { 'Accept': 'application/json' },
      body: new FormData(this),
    });

    if (res.ok) {
      const successEl = document.getElementById('formSuccess');
      successEl.textContent = translations[currentLang]['form-success'];
      successEl.classList.add('show');
      this.reset();
    } else {
      alert('Erro ao enviar. Tente novamente.');
    }
  } catch {
    alert('Erro de conexão. Tente novamente.');
  } finally {
    btn.textContent = translations[currentLang]['form-submit'];
    btn.disabled = false;
  }
});

